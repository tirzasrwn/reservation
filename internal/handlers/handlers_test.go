func getCtx(req *http.Request) context.Context {
	ctx, err := session.Load(req.Context(), req.Header.Get("X-Session"))
	if err != nil {
		log.Println(err)
	}
	return ctx
}

func constructReqBody(params map[string]string) string {
	reqBody := ""
	for key, value := range params {
		if reqBody == "" {
			reqBody = fmt.Sprintf("%s=%s", key, value)
		} else {
			reqBody = fmt.Sprintf("%s&%s=%s", reqBody, key, value)
		}
	}
	return reqBody
}
